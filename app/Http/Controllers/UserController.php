     */
    public function show($id)
    {
        $user = DB::table('users')->where('id', $id)->get();
        return view('dashboard.user.edit', ['user' => $user]);
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return Response
     */
